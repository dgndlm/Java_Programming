package day02_escapeSequences;

public class EscapeSequences {

    public static void main(String[] args) {
        System.out.println("Hello Students\nToday We Have Java Class");

        System.out.println("------------------------------------------------------------------------------------------");

        System.out.println("\tJava Programming Language");
        System.out.println("\t\tJava Developer Course Batch 1");

        System.out.println("------------------------------------------------------------------------------------------");
        System.out.println("\\Dilem");


        System.out.println("------------------------------------------------------------------------------------------");

        System.out.println("My favorite Tv show is \"The Walking Dear\"");
    }
}

/*
     1. \n : New Line
     2. \t : Paragraph Space
     3. \\ : Back slash
     4. \" : Double quote
 */